#iteration loop (while)
counter = 0
while counter < 5:
    print(counter)
    counter += 1

#for loop
my_fruit = ["Apple", "Banana", "Orange"]

for fruit in my_fruit:
    print(fruit.upper())

#numeric range
tuple(range(5))
#range(start, stop)
tuple(range(2, 5))
#range(start, stop, step)
tuple(range(1, 5, 2))



#tutorial on while loop
count = 0
name = str(input("What is your name?"))

while count < 5:
    print(name, "is awesome!")
    count += 1

#for loop tutorial
for i in range(5, 11):
    print(i)

